#include <unistd.h>

void	ft_is_negative(int n)
{
	char	negativo;	
	char	positivo;

	negativo = 'N';
	positivo = 'P';
	if (n < 0)
	{
		write(1, &negativo, 1);
	}
	else
	{
		write(1, &positivo, 1);
	}
}
